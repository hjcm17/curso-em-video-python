# Crie um programa que leia dois números e mostre a soma entre eles
#Saída
print('Esse programa realiza a soma entre dois números...')
#Entrada
num1 = int(input('Digite o primeiro número: '))# colocar o tipo primitivo antes do input
num2 = int(input('Digite o segundo número: '))
#Processamento
soma = num1+num2
print('{} + {} = {}'.format(num1,num2,soma))
